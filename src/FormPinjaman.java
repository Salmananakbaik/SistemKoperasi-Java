private void tblPinjamanMouseClicked(java.awt.event.MouseEvent evt) {
    try {
        int baris = tblPinjaman.getSelectedRow();

        txtId.setText(tblPinjaman.getValueAt(baris, 0).toString());
        txtNominal.setText(tblPinjaman.getValueAt(baris, 3).toString());
        txtTenor.setText(tblPinjaman.getValueAt(baris, 4).toString());
        cmbStatus.setSelectedItem(tblPinjaman.getValueAt(baris, 5).toString());

        java.util.Date tgl = new SimpleDateFormat("yyyy-MM-dd")
                .parse(tblPinjaman.getValueAt(baris, 2).toString());
        dcTanggal.setDate(tgl);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal pilih data: " + e.getMessage());
    }
    }//GEN-LAST:event_BtnKembaliActionPerformed

