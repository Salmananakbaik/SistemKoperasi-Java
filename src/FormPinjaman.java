    private String getTanggal() {
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    return sdf.format(dcTanggal.getDate());
}

private void loadAnggota() {
    try {
        cmbAnggota.removeAllItems();
        cmbAnggota.addItem("Pilih Anggota");

        Connection conn = koneksi.getKoneksi();
        String sql = "SELECT id_anggota, nama FROM anggota";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            cmbAnggota.addItem(rs.getString("id_anggota") + " - " + rs.getString("nama"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load anggota: " + e.getMessage());
    }
}

private String ambilIdAnggota() {
    String item = cmbAnggota.getSelectedItem().toString();
    return item.split(" - ")[0];
}

private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Pinjaman");
    model.addColumn("Nama Anggota");
    model.addColumn("Tanggal");
    model.addColumn("Nominal");
    model.addColumn("Tenor");
    model.addColumn("Status");

    try {
        String sql = "SELECT p.id_pinjaman, a.nama, p.tanggal, p.nominal, p.tenor, p.status "
                + "FROM pinjaman p JOIN anggota a ON p.id_anggota = a.id_anggota";

        Connection conn = koneksi.getKoneksi();
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_pinjaman"),
                rs.getString("nama"),
                rs.getString("tanggal"),
                rs.getString("nominal"),
                rs.getString("tenor"),
                rs.getString("status")
            });
        }

        tblPinjaman.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
    }
}

